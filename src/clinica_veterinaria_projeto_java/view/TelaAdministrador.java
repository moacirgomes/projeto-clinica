    public void preencherTabela() {
        ArrayList dados = controller.lista();
        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        tbAdministradores.setModel(modelo);
        tbAdministradores.getColumnModel().getColumn(0).setPreferredWidth(53);
        tbAdministradores.getColumnModel().getColumn(0).setResizable(false);
        tbAdministradores.getColumnModel().getColumn(1).setPreferredWidth(270);
        tbAdministradores.getColumnModel().getColumn(1).setResizable(false);
        tbAdministradores.getColumnModel().getColumn(2).setPreferredWidth(270);
        tbAdministradores.getColumnModel().getColumn(2).setResizable(false);
        tbAdministradores.getColumnModel().getColumn(3).setPreferredWidth(0);
        tbAdministradores.getColumnModel().getColumn(3).setResizable(false);
        tbAdministradores.getTableHeader().setReorderingAllowed(false);
        tbAdministradores.setAutoResizeMode(tbAdministradores.AUTO_RESIZE_OFF);
        tbAdministradores.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }

    public void resetInputs() {
        txtNome.setEnabled(false);
        txtUsuario.setEnabled(false);
        txtSenha.setEnabled(false);

        txtNome.setText("");
        txtUsuario.setText("");
        txtSenha.setText("");
    }
