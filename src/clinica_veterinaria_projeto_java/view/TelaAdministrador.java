    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        String senha = new String(txtSenha.getPassword());
        if (idTemp == 0) {
            //novo adm
            
            if (txtNome.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Informe o nome do administrador.");
            }else if (txtUsuario.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Informe o usuario.");
            }else if (txtSenha.getPassword().equals("")) {
                JOptionPane.showMessageDialog(null, "Informe a senha.");
            }  else {
                Administrador adm = new Administrador(txtNome.getText(), txtUsuario.getText(), senha);
                controller.cadastrar(adm);
                this.resetInputs();
                this.preencherTabela();
                btnSalvar.setEnabled(false);

            }
        } else {

            //editar veterinario
            if (txtNome.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Informe o nome do administrador.");
            }else if (txtUsuario.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Informe o usuario.");
            }else if (txtSenha.getPassword().equals("")) {
                JOptionPane.showMessageDialog(null, "Informe a senha.");
            }  else {
                Administrador adm = new Administrador(idTemp,txtNome.getText(), txtUsuario.getText(), senha);
                controller.editar(adm);
                this.resetInputs();
                this.preencherTabela();
                btnNovo.setEnabled(true);
                btnSalvar.setEnabled(false);
                idTemp = 0;
            }

        }

    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed

        int linha = tbAdministradores.getSelectedRow();
        if (linha == -1) {
            JOptionPane.showMessageDialog(rootPane, "Selecione uma linha da tabela.");
        } else {
            int id = (int) tbAdministradores.getValueAt(linha, 0);
            idTemp = id;

            txtNome.setEnabled(true);
            txtNome.setText((String) tbAdministradores.getValueAt(linha, 1));

            txtUsuario.setEnabled(true);
            txtUsuario.setText((String) tbAdministradores.getValueAt(linha, 2));

            txtSenha.setEnabled(true);
            txtSenha.setText((String) tbAdministradores.getValueAt(linha, 3));

            btnSalvar.setEnabled(true);
            btnNovo.setEnabled(false);
        }

    }//GEN-LAST:event_btnEditarActionPerformed

